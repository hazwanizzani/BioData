public class Rectangle {
    public static void main(String[] args) {

        double width = 24.25f;
        double length = 32.00f;

        double area;

        area = width * length;

        System.out.println("Area of Rectangle \n" +
                " ----------------------------\n" +
                "Width: " + width +"cm \n" +
                "Length: " + length + "cm \n" +
                "Area: " + area + "cm^2");
    }
}
